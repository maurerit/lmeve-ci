DROP TABLE `apilocations`;

CREATE TABLE IF NOT EXISTS `apilocations` (
	`itemID` BIGINT(20) NOT NULL,
	`itemName` VARCHAR(256) NOT NULL,
	`x` DOUBLE NOT NULL,
	`y` DOUBLE NOT NULL,
	`z` DOUBLE NOT NULL,
	`corporationID` INT(11) NOT NULL,
	PRIMARY KEY (`itemID`)
) ENGINE=MyISAM DEFAULT CHARSET=utf8 ;